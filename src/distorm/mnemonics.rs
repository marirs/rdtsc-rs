// #[cfg(feature = "distorm_light")]
#[repr(C)]
#[derive(Copy, Clone)]
pub struct WMnemonic {
	pub length: u8,
	pub p: [u8; 1], // p is a null terminated string, which contains 'length' characters.
}

// #[cfg(feature = "distorm_light")]
#[repr(C)]
#[derive(Copy, Clone)]
pub struct WRegister {
	pub length: u32,
	pub p: [u8; 6], // p is a null terminated string.
}

extern "C" {
	static MNEMONICS: *mut [u8];
	static REGISTERS: *mut [WRegister];
}

pub unsafe fn get_register_name(register: usize) -> Option<*const [u8]> {
	if register < (*REGISTERS).len() {
		Some(&(*REGISTERS)[register].p)
	} else {
		None
	}
}

pub unsafe fn get_mnemonic_name(mnemonic: usize) -> Option<*const [u8]> {
	if mnemonic < (*MNEMONICS).len() {
		Some(&(*((*MNEMONICS)[mnemonic] as *const WMnemonic)).p)
	} else {
		None
	}
}

